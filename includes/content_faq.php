<br/>FAQ (Remote Classroom Training with a Live Trainer over the Internet) <br/><br/>
<div class="question">• Is Remote Classroom training the same as physically being in the classroom?</div>
<div class="answer">
i) Students receiving training through the Remote Classroom Training environment will have the same experience as they would in a physical classroom.<br/>
ii) Student can Participate in class from anywhere in the world with a compatible computer and reliable access to the Internet.<br/>
iii) Student can Interact with the Trainer and other students through a virtual classroom interface.<br/>
</div><br/>

<div class="question">• What are the system requirements for Remote Classroom Training?</div>
<div class="answer">
Generally any system that has required software installed and is connected to the Internet will work with Remote Classroom Training. However the system should be tested to make sure headset, speaker, microphone, webcam, audio and video are working.<br/>
</div><br/>

<div class="question">• How does the audio and video work for participating in Remote Classroom Training?</div>
<div class="answer">
Audio is available to the students and Trainer over the Internet (no phone lines are required). Students will access the audio of the classroom through their computers. This involves wearing a headset that has a microphone. Video (via web cams) is an optional feature in the classroom. The Trainer may broadcast a video feed for presentations and lecture.<br/>
</div><br/>

<div class="question">• Do I need a phone to participate in the training?</div>
<div class="answer">
No, Audio communications for the Remote Classroom Training will happen over the Internet. Student will need a headset connected to their computers to be able to hear and communicate with the Trainer and other students.<br/>
</div><br/>

<div class="question">• Will I be able to see the Trainer during the training?</div>
<div class="answer">
Most of the time students will not see the Trainer via video. However there may be moments during lecture sessions where the Trainer chooses to broadcast a video feed. Even though video may not be always active students will still see the instructional slides and graphics. The Trainer may also use the white board and application sharing features of the virtual classroom to provide additional visual reference to the materials taught.<br/>
</div><br/>

<div class="question">• What happens if I have a question for the Trainer?</div>
<div class="answer">
Student who wishes to interact with the Trainer may do so by any number of methods. The primary method to ask questions is to just speak to the Trainer through your headset microphone. Other commonly used collaboration tools are classroom chat (public and private), "raise hand" etc.<br/>
</div><br/>

<div class="question">• What time does class start and in which time zone?</div>
<div class="answer">
Class start time and time zone are indicated in the program menu under the program detail. Generally class start time will be either early morning (7~8 AM IST) or late evening (8~9 PM IST) during weekdays and 10~12 AM IST during weekends.<br/><br/>

